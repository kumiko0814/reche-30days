/* 今日（カレンダー日付）の一言＋写真。daily上書き＞日めくりローテーション */
window.MORIKUMI_TODAY = function () {
  const M = window.MORIKUMI;
  const today = (window.RECHE && RECHE.todayISO) ? RECHE.todayISO()
              : new Date().toISOString().slice(0, 10);
  if (M.daily && M.daily[today]) return Object.assign({ date: today }, M.daily[today]);
  if (!M.lines.length) return Object.assign({ date: today }, M.fallback);
  let idx = 0;
  if (window.RECHE && RECHE.daysBetween) {
    idx = RECHE.daysBetween(M.epoch, today);
    if (idx < 0) idx = 0;
  }
  return {
    date:  today,
    text:  M.lines[idx % M.lines.length],
    photo: M.photos.length ? M.photos[idx % M.photos.length] : (M.fallback.photo || "")
  };
};
